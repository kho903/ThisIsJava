package exercise.exam17;

public class Printer {

	static void println(int val) {
		System.out.println(val);
	}

	static void println(boolean val) {
		System.out.println(val);
	}

	static void println(double val) {
		System.out.println(val);
	}

	static void println(String val) {
		System.out.println(val);
	}

}
